   private void setAxis()
   {
      if (xButton.isSelected())
         params.setAxis(0);
      if (yButton.isSelected())
         params.setAxis(1);
      if (zButton.isSelected())
         params.setAxis(2);
      resetTransform();
   }

   public void setInField(Field inField)
   {
      this.inField = inField;
      transformPanel.setTransSensitivity(inField.getDiameter()/1000);
   }

