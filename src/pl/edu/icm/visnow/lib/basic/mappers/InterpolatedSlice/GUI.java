   /**
    * Set the value of params
    *
    * @param params new value of params
    */
   public void setParams(Params params)
   {
      this.params = params;
      transformPanel.setTransformParams(params.getTransform());
      use3DPickBtn.setSelected(params.getPick3DListener().isActive());
      hoeKnifePickSwitch.setSelected(params.getPick3DListener().getPickType() == PickType.PLAIN_HOE);
   }
   
   public void setSignalingTransform(SignalingTransform3D sigTrans)
   {
      transformPanel.setSigTrans(sigTrans);
   }

   public void resetTransform()
   {
      transformPanel.resetTransform();
   }

   public void creatingTreeInfo(boolean exec)
   {
      if (exec)
         treeCreationLabel.setText("Creating cell tree. Please wait.");
      else
         treeCreationLabel.setText("Cell tree created.");
   }
   
