   public void setParams(Params params)
   {
      params.setSigma(sigmaSlider.getVal());
      params.setNThreads(VisNow.availableProcessors());
      params.setSigma(sigmaSlider.getVal());
      params.setSigma1(sigma1Slider.getVal());
      params.setHiAbs(absHighBox.isSelected());
      params.setHiPass(highPassBox.isSelected());
      params.setBandPass(bandPassBox.isSelected());
      params.setBandAbs(absBandBox.isSelected());
      this.params = params;
   }
   
   private void fireStateChanged()
   {
      params.setSigma(sigmaSlider.getVal());
      params.setNThreads(VisNow.availableProcessors());
      params.setSigma(sigmaSlider.getVal());
      params.setSigma1(sigma1Slider.getVal());
      params.setHiAbs(absHighBox.isSelected());
      params.setHiPass(highPassBox.isSelected());
      params.setBandPass(bandPassBox.isSelected());
      params.setBandAbs(absBandBox.isSelected());
      params.setActive(true);
   }
   

