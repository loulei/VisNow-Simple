   public static long hash(InputStream is) throws IOException
   {
      long hashValue = 0;
      int bytesRead;
      byte[] buffer = new byte[READ_BUFFER_SIZE];
      while ((bytesRead = is.read(buffer)) > 0)
         hashValue = hash(buffer, 0, bytesRead, hashValue);
      return hashValue;
   }

