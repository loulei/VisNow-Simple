   private void goButtonActionPerformed(ActionEvent evt)//GEN-FIRST:event_goButtonActionPerformed
   {//GEN-HEADEREND:event_goButtonActionPerformed
      try
      {
         currentURL = urlField.getText();
         history.add(currentURL);
         setPage(currentURL);
      } catch (Exception ex)
      {
      }
   }//GEN-LAST:event_goButtonActionPerformed

   private void backButtonActionPerformed(ActionEvent evt)//GEN-FIRST:event_backButtonActionPerformed
   {//GEN-HEADEREND:event_backButtonActionPerformed
      try
      {
         currentURL = history.lastElement();
         history.removeElement(currentURL);
         currentURL = history.lastElement();
         setPage(currentURL);
         urlField.setText(currentURL);
         if (history.size() == 1)
            backButton.setEnabled(false);
      } catch (Exception ex)
      {
      }   
   }//GEN-LAST:event_backButtonActionPerformed

   private void readButtonActionPerformed(ActionEvent evt)//GEN-FIRST:event_readButtonActionPerformed
   {//GEN-HEADEREND:event_readButtonActionPerformed
      setVisible(false);
      fireStateChanged();
   }//GEN-LAST:event_readButtonActionPerformed

   private void browserPaneHyperlinkUpdate(HyperlinkEvent evt)//GEN-FIRST:event_browserPaneHyperlinkUpdate
   {//GEN-HEADEREND:event_browserPaneHyperlinkUpdate
      if (evt.getEventType() == HyperlinkEvent.EventType.ACTIVATED)
      {
         try
         {
            currentURL = evt.getURL().toString();
            history.add(currentURL);
            backButton.setEnabled(true);
            setPage(currentURL);
            urlField.setText(currentURL);
         } catch (Exception e)
         {
            System.out.println("ERROR: Trouble fetching url");
         }
      }
   }//GEN-LAST:event_browserPaneHyperlinkUpdate

