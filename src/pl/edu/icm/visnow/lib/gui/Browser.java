   protected final void setPage(String url)
   {
      try
      {
         readButton.setEnabled(false);
         for (String extension : extensions)
         {
            if (url.endsWith(extension))
               readButton.setEnabled(true);
         }
         browserPane.setPage(url);
      } catch (IOException e)
      {
      }
   }

   public String getCurrentURL()
   {
      return currentURL;
   }

